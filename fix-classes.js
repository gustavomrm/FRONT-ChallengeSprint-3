// Fun√ß√£o para processar um arquivo
function processFile(filePath) {
  try {
    let content = fs.readFileSync(filePath, 'utf8');
    let modified = false;
    
    // Aplicar todas as substitui√ß√µes
    for (const [oldClass, newClass] of Object.entries(classMappings)) {
      const regex = new RegExp(`\\b${oldClass}\\b`, 'g');
      if (content.includes(oldClass)) {
        content = content.replace(regex, newClass);
        modified = true;
      }
    }
    
    if (modified) {
      fs.writeFileSync(filePath, content, 'utf8');
      console.log(`‚úÖ Processed: ${filePath}`);
    }
  } catch (error) {
    console.error(`‚ùå Error processing ${filePath}:`, error.message);
  }
}

// Fun√ß√£o para processar diret√≥rio recursivamente
function processDirectory(dirPath) {
  const items = fs.readdirSync(dirPath);
  
  for (const item of items) {
    const fullPath = path.join(dirPath, item);
    const stat = fs.statSync(fullPath);
    
    if (stat.isDirectory()) {
      processDirectory(fullPath);
    } else if (item.endsWith('.tsx') || item.endsWith('.ts')) {
      processFile(fullPath);
    }
  }
}

